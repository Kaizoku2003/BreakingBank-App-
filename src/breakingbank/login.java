    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        String nombre = jTextField1.getText().trim();
        String password = new String(jPasswordField1.getPassword());

        if (nombre.isEmpty() || password.isEmpty()) {
            JOptionPane.showMessageDialog(this,
                    "Por favor, complete todos los campos.",
                    "Campos incompletos",
                    JOptionPane.WARNING_MESSAGE);
            return;
        }

        Usuario u = authService.iniciarSesion(nombre, password);

        if (u != null) {
            Usuario.usuarioLogueado = u;
            JOptionPane.showMessageDialog(this,
                    "Inicio de sesi贸n exitoso. Bienvenido, " + u.getNombreCompleto() + "!",
                    "Bienvenido",
                    JOptionPane.INFORMATION_MESSAGE);

            // Ir al men煤 principal
            new Menu().setVisible(true);
            this.dispose();
        } else {
            JOptionPane.showMessageDialog(this,
                    "Nombre o contrase帽a incorrectos.",
                    "Error de inicio de sesi贸n",
                    JOptionPane.ERROR_MESSAGE);
        }

    }//GEN-LAST:event_jButton1ActionPerformed

